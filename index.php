    document.addEventListener('DOMContentLoaded', function() {
      const nav = document.getElementById('navmenu');
      if (!nav) return;

      // select only links that point to anchors
      const navLinks = Array.from(nav.querySelectorAll('a[href^="#"]'));
      // map nav links to their target sections (filter out missing)
      const sections = navLinks
        .map(l => document.querySelector(l.getAttribute('href')))
        .filter(Boolean);

      // helper: clear active classes on both <a> and its parent <li> (covers both CSS styles)
      function clearActive() {
        navLinks.forEach(l => {
          l.classList.remove('active');
          if (l.parentElement) l.parentElement.classList.remove('active');
        });
      }

      // IntersectionObserver options
      // rootMargin uses negative top/bottom so the "middle" of viewport triggers the active state.
      const observerOptions = {
        root: null,
        rootMargin: '-40% 0px -40% 0px',
        threshold: 0
      };

      // main observer
      if ('IntersectionObserver' in window && sections.length) {
        const observer = new IntersectionObserver((entries) => {
          entries.forEach(entry => {
            if (entry.isIntersecting) {
              const id = entry.target.id;
              const link = nav.querySelector(`a[href="#${id}"]`);
              if (link) {
                clearActive();
                link.classList.add('active');
                if (link.parentElement) link.parentElement.classList.add('active');
              }
            }
          });
        }, observerOptions);

        sections.forEach(s => observer.observe(s));
      } else {
        // fallback: simple scroll spy if IntersectionObserver not supported
        const onScroll = () => {
          let currentId = '';
          const halfway = window.innerHeight / 2;
          for (const s of sections) {
            const rect = s.getBoundingClientRect();
            if (rect.top <= halfway && rect.bottom >= halfway) {
              currentId = s.id;
              break;
            }
          }
          clearActive();
          if (currentId) {
            const link = nav.querySelector(`a[href="#${currentId}"]`);
            if (link) {
              link.classList.add('active');
              if (link.parentElement) link.parentElement.classList.add('active');
            }
          }
        };
        window.addEventListener('scroll', onScroll, {
          passive: true
        });
        onScroll();
      }

      // Optional: smooth scroll and prevent jump; adjust headerOffset if you have a fixed header
      const headerOffset = 0; // set to header height (px) if header is fixed, e.g. 70
      navLinks.forEach(link => {
        link.addEventListener('click', (e) => {
          const target = document.querySelector(link.getAttribute('href'));
          if (!target) return;
          e.preventDefault();
          const top = target.getBoundingClientRect().top + window.pageYOffset - headerOffset;
          window.scrollTo({
            top,
            behavior: 'smooth'
          });
          // update URL hash without instant jump
          history.pushState(null, '', link.getAttribute('href'));
        });
      });

